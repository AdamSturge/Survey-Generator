package model;

import java.io.Serializable;

public class PyramidSquare implements Serializable{
	int column;
	int row;
	int answer; //answer currently occupying this square, -1 indicate unoccupied
	
	public PyramidSquare(int c, int r, int a) {
		column = c;
		row = r;
		answer = a;
	}
	
	public PyramidSquare(int c, int r){
		column = c;
		row = r;
		answer = -1;
	}

	public int getAnswer() {
		return answer;
	}

	public int getColumn() {
		return column;
	}

	public void setColumn(int column) {
		this.column = column;
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public void setAnswer(int answer) {
		this.answer = answer;
	}
}
